function normalizarTexto(texto: string): string {
  return texto
    .toLowerCase()
    .normalize('NFD')
    .replace(/[̀-ͯ]/g, '')
    .trim()
}

export function buscarAlimento(query: string): (typeof ALIMENTOS)[number] | null {
  const q = normalizarTexto(query)

  // Exact match first
  const exacto = ALIMENTOS.find((a) => normalizarTexto(a.nombre) === q)
  if (exacto) return exacto

  // Partial match
  const parcial = ALIMENTOS.find((a) => normalizarTexto(a.nombre).includes(q))
  if (parcial) return parcial

  // Reverse partial: query includes the food name
  const inverso = ALIMENTOS.find((a) => q.includes(normalizarTexto(a.nombre)))
  if (inverso) return inverso

  return null
}

export function buscarEjercicio(query: string): (typeof EJERCICIOS)[number] | null {
  const q = normalizarTexto(query)

  const exacto = EJERCICIOS.find((e) => normalizarTexto(e.nombre) === q)
  if (exacto) return exacto

  const parcial = EJERCICIOS.find((e) => normalizarTexto(e.nombre).includes(q))
  if (parcial) return parcial

  const inverso = EJERCICIOS.find((e) => q.includes(normalizarTexto(e.nombre)))
  if (inverso) return inverso

  return null
}

export function calcularCaloriasEjercicio(
  nombre: string,
  series: number,
  reps: number
): number {
  const ejercicio = buscarEjercicio(nombre)
  if (!ejercicio) return 0
  return Math.round(series * reps * ejercicio.caloriasPorUnidad)
}
